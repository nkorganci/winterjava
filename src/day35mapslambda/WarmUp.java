package day35mapslambda;

public class WarmUp {
    /*
    Create a method to print integers from 0 to 100.
     */
    public static void main(String[] args) {

        char c1 = 'e' - 'e';
        char c2 = 'e';

        for (char i = c1; i < c2; i++) {
            System.out.println((int) i);
        }
    }
}
